#pragma once

#define LKCOMMON_PI 3.14159265358979323846
#define LKCOMMON_PIF 3.1415926535f

#define LKCOMMON_DEG_TO_RAD(x) (x / 180.0 * LKCOMMON_PI)
#define LKCOMMON_DEG_TO_RADF(x) (x / 180.0f * LKCOMMON_PIF)

#define LKCOMMON_RAD_TO_DEG(x) (x / LKCOMMON_PI * 180.0)
#define LKCOMMON_RAD_TO_DEGF(x) (x / LKCOMMON_PIF * 180.0f)
